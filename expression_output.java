
public class expression_output {

	public static void main(String[] args) {
		
		System.out.println("The java program will display the output\n"
				+ "of the following expressions: ");
	    System.out.println("1/2 equals " + 1/2);
	    System.out.println("1%2 equals " + 1%2);
	    System.out.println("1.0/2 equals " + 1.0/2);
	    System.out.println("5 + 7/2 equals " + (5+7/2));
	    System.out.println("Beat + Army equals "
	    		+ "Beat" + ' ' + "Army");
	    System.out.println("6 + 13 / 5 - 35 % 3 equals " 
	    		+ (6 + 13 / 5 - 35 % 3));
	    System.out.println("3.5 * (5/4) equals " + (3.5 * (5/4)));
	    System.out.println("(3.5 * 5)/4 equals " + ((3.5 * 5)/4));

	}

}
